const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const ecommerceUser = mongoose.model("ecommerceUser", userSchema);

module.exports = ecommerceUser;
